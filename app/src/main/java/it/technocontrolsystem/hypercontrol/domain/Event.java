package it.technocontrolsystem.hypercontrol.domain;

/**
 * Domain Class for a single event
 */

//federico
public class Event {
    private int id;
    private String data;
    private int tipo;
    private String testo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public String getTesto() {
        return testo;
    }

    public void setTesto(String testo) {
        this.testo = testo;
    }
}
